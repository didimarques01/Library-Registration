    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        RegistroDAO rdao = new RegistroDAO();
        Registrar registrar = new Registrar();
        
        ComputadorDAO cdao = new ComputadorDAO();
        Computador computador = new Computador();
        
        List<Registro> lista = rdao.read();
        List<Computador> lista2 = cdao.read();
        
        int cont = 1;
        
        System.out.println(tableComputadores.getRowCount());
        
        for (Computador c: lista2) {
            if (tableComputadores.getRowCount() != 0){
                if (c.getDisponibilidade().toString().equals("Não disponível") || c.getStatus().toString().equals("Defeituoso")) {
                    cont++;
                } else if (c.getDisponibilidade().toString().equals("Disponível") && c.getStatus().toString().equals("Funcionando")) {
                    cont = 0;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum computador válido registrado");
                break;
            }
        }
        if (cont == 0) {
            new Registrar().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(null, "Nenhum computador válido registrado");
            }
    }//GEN-LAST:event_jButton2ActionPerformed

