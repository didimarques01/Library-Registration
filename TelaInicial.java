    public void readJTable() {
        DefaultTableModel modelo = (DefaultTableModel) tableComputadores.getModel();
        modelo.setNumRows(0);
        ComputadorDAO cdao = new ComputadorDAO();
        
        for (Computador c: cdao.read()) {
            modelo.addRow(new Object[]{
               c.getId(),
               c.getDisponibilidade(),
               c.getStatus()
            });
        }
    }

