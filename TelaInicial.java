    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:  
           if(tableComputadores.getSelectedRow() != -1){
               
               try {
                   Computador c = new Computador();
                   ComputadorDAO dao = new ComputadorDAO();
                   
                   c.setId(txtID.getText());
                   c.setStatus((String) cbStatus.getSelectedItem());
                   c.setDisponibilidade((String) cbDisponivel.getSelectedItem());
                   
                   dao.update(c);
                   
                   txtID.setText("");
                   cbDisponivel.setSelectedItem("");
                   cbStatus.setSelectedItem("");
                   
                   readJTable();
               } catch (SQLException ex) {
                   Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
               }
           
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
       if(tableComputadores.getSelectedRow() != -1){
            try {
                   Computador c = new Computador();
                   ComputadorDAO dao = new ComputadorDAO();
                   
                   c.setId(txtID.getText());
                   
                   dao.delete(c);
                   
                   txtID.setText("");
                   cbDisponivel.setSelectedItem("");
                   cbStatus.setSelectedItem("");
                   
                   readJTable();
               } catch (SQLException ex) {
                   Logger.getLogger(TelaInicial.class.getName()).log(Level.SEVERE, null, ex);
               }
       
       } else{
           JOptionPane.showMessageDialog(null, "Selecione uma linha para ser excluida.");
       }
    }//GEN-LAST:event_jButton4ActionPerformed

